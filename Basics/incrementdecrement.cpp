#include<iostream>
using namespace std;
int main(){
    // incrment and decrement
    int x = 5;
    cout<<x<<endl; 
    x = x+1;
    cout<<x<<endl;
    ++x; // pre increment 
    cout<<x<<endl;
    x++; // post increment
    cout<<x<<endl;
    x--; // x = x-1
    cout<<x<<endl;
    --x; // x = x-1
    cout<<x<<endl;
    /*
    multi line comments

    */
   cout<<--x<<endl;
   cout<<x--<<endl;
   cout<<x;
}